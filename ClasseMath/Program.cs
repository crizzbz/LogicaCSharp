using System;


namespace ClasseMath
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Valor Máximo: " + Math.Max(5, 10)); // Valor Máximo
            Console.WriteLine("Valor Mínimo: " + Math.Min(5, 10)); // Valor Mínimo

            Console.WriteLine("Raiz quadrada: " + Math.Sqrt(64)); //Raiz quadrada
            Console.WriteLine("Arrendondamento: " + Math.Round(9.99)); // Arredondamento
        }
    }
}
